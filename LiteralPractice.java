
/**
 * literalpractice.java
 * literal Java's 8 primitive data types.
 *
 * @author (your name)
 * @version (a version number or date)
 */public class LiteralPractice {
    public static void main(String[] args) {
        long bigNumber = 1234567890123L;      
        float piApprox = 3.14159f;           
        char copyrightSymbol = '\u00A9';     

        System.out.println("Long value: " + bigNumber);
        System.out.println("Float value: " + piApprox);
        System.out.println("Char value: " + copyrightSymbol);
    }
}